    private void savePeminjamanBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savePeminjamanBtnActionPerformed
        // TODO add your handling code here:
        peminjaman pem = new peminjaman();
        pem.setPem(idPeminjamanField.getText());
        pem.setPel (idPelangganField.getText());
        pem.setMobil("" + jenisMobilComboBox.getSelectedItem());
        pem.setPtanggal(""+tanggalComboBox.getSelectedItem());
        pem.setPbulan(""+bulanComboBox.getSelectedItem());
        pem.setPtahun(tahunField.getText());
        pem.setKtahun(tahunField1.getText());
        pem.setLama(lamaField.getText());
        pem.setPegawai(idPegawaiField.getText());
        pem.setBiaya(sewaField.getText());

        pem.insertPeminjaman();
        javax.swing.JOptionPane.showMessageDialog(null, "Insert Success");

        idPeminjamanField.setText(null);
        idPelangganField.setText(null);
        jenisMobilComboBox.setSelectedIndex(0);
        tanggalComboBox.setSelectedIndex(0);
        bulanComboBox.setSelectedIndex(0);
        tahunField.setText(null);
        tahunField1.setText(null);
        lamaField.setText(null);
        idPegawaiField.setText(null);
        sewaField.setText(null);
        totalSewaField.setText(null);
    }//GEN-LAST:event_savePeminjamanBtnActionPerformed

