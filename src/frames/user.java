    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        koneksi kon = new koneksi();
        ResultSet rs = null;

        try {
            rs = kon.getData("select * from pelanggan where id_pelanggan = '" + idPelangganSearchField.getText() + "'");
            while(rs.next()){
                idPelangganField1.setText(rs.getString("id_pelanggan"));
                namaPelangganField1.setText(rs.getString("nama"));
                alamatPelangganField1.setText(rs.getString("alamat"));
                cpPelangganField1.setText(rs.getString("cp"));
                javax.swing.JOptionPane.showMessageDialog(null, "Search Success");
                idPelangganSearchField.setText(null);
                namaPelangganField1.setEnabled(true);
                alamatPelangganField1.setEnabled(true);
                cpPelangganField1.setEnabled(true);
                updateBtn.setEnabled(true);
                deleteBtn.setEnabled(true);
            }
            rs.close();
        } catch (Exception e){
            javax.swing.JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

