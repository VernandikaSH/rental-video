    private void generatePeminjamanBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generatePeminjamanBtnActionPerformed
        // TODO add your handling code here:
        koneksi kon = new koneksi();
        ResultSet rs = null;
        try {
            rs = kon.getData("select * from mobil where id_mobil = '" + jenisMobilComboBox.getSelectedItem() + "'");
            while(rs.next()){
                sewaField.setText(rs.getString("sewa"));
                int k, l, m, n, o;
                k = Integer.valueOf(lamaField.getText()).intValue();
                l = Integer.valueOf("" + tanggalComboBox.getSelectedItem()).intValue();
                n = Integer.valueOf(sewaField.getText()).intValue();
                m = k + l;
                o = k * n;
                totalSewaField.setText(String.valueOf(o));
                tahunField1.setText(String.valueOf(m) + "/" + bulanComboBox.getSelectedItem() + "/" + tahunField.getText());
            }
            rs.close();
        }
        catch(Exception e){

        }
    }//GEN-LAST:event_generatePeminjamanBtnActionPerformed

